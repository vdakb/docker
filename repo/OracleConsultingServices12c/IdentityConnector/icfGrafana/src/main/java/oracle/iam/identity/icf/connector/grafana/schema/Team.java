  public final boolean equals(final Object other) {
    if (this == other)
      return true;

    if (other == null || getClass() != other.getClass())
      return false;

    final Team that = (Team)other;
    return Objects.equals(this.id, that.id) && Objects.equals(this.name, that.name);
  }
